public class revision{

    private String banco;
    private int Sede;

    public revision(String banco, int Sede) {
        this.banco = banco;
        this.Sede = Sede;
    }

    public String getBanco() {
        return banco;
    }

    public void setBanco(String banco) {
        this.banco = banco;
    }

    public int getSede() {
        return Sede;
    }

    public void setSede(int Sede) {
        this.Sede = Sede;
    }

    

}